/******************************************************************************************/
/**************************************** Defines *****************************************/
/******************************************************************************************/
#define LED_ON          (0x00000001UL)
/**
 * @note : In GPIO, I use BSRR, So I want to control Bit16:Bit32 to Clear the Pins
 */
#define LED_OFF         (0x00010000UL)
#define LED_FORWARD     (0x00000000UL)
/**
 * ON Xor Reverse : Clear the Pin (Write High on Bit16:Bit32 in BSRR)
 *  > 00000001 XOR 00010001 = 00010000, So, the Pin Become LOW.
 * OFF Xor Reverse: Set The Pin   (Write High on Bit0:Bit15 in BSRR)
 *  > 00010000 XOR 00010001 = 00000001, So, The pin Become HIGH
 */
#define LED_REVERSE     (0x00010001UL)
/******************************************************************************************/
/*************************************** Data Types ***************************************/
/******************************************************************************************/
